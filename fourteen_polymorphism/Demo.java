package fourteen_polymorphism;

public class Demo {

    public static void main(String[] args) {
        
        int num1 = 10;
        int num2 = 20;

        System.out.println("Sum is: "+(num1+num2));

        String s1 = "Hello ";
        String s2 = "World";

        System.out.println("Msg is: "+(s1+s2));

        // Sum is: 30
        // Msg is: Hello World

        // NOTE - Observation
        // + operator - when using numbers - adding numbers
        // + operator - when using strings - concatenation (joining)


    }

}
